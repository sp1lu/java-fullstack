public class whileLoop {
    public static void main(String[] args) {
        int i = 0; // valore iniziale della variabile i

        while (i <= 10) { // procedi finchè il valore di i è inferiore o uguale a 10
            System.out.println(i); // stampa il valore attuale di i
            i++; // incrementa il valore di i di una unità
        }
    }
}
